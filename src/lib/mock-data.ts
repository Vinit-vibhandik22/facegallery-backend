const generatePhotos = (projectId: string, startId: number, count: number): Photo[] => {
    return Array.from({ length: count }, (_, i) => ({
        id: `photo-${projectId}-${i}`,
        projectId,
        url: photo(startId + i, 1200, 800),
        thumbnailUrl: thumb(startId + i),
        faceCount: Math.floor(Math.random() * 4) + 1,
        width: 1200,
        height: 800,
        createdAt: '2026-02-15T08:30:00Z',
    }));
};

